test("builds transcript cache keys from the exact selected track identity", () => {
  const chineseTrack = {
    baseUrl: "https://www.youtube.com/api/timedtext?v=dQw4w9WgXcQ&lang=zh-Hans",
    languageCode: "zh-Hans",
    vssId: ".zh-Hans",
    kind: "manual"
  };
  const englishTrack = {
    baseUrl: "https://www.youtube.com/api/timedtext?v=dQw4w9WgXcQ&lang=en",
    languageCode: "en",
    vssId: "a.en",
    kind: "auto",
    isAutoGenerated: true
  };

  assert.equal(
    transcript.createTranscriptCacheKey("dQw4w9WgXcQ"),
    "transcript:dQw4w9WgXcQ:auto:none:none:direct:none:"
  );
  assert.match(
    transcript.createTranscriptCacheKey("dQw4w9WgXcQ", {
      baseUrl: "https://www.youtube.com/api/timedtext?v=dQw4w9WgXcQ&lang=es",
      languageCode: "es",
      vssId: ".es",
      kind: "translated",
      isTranslated: true,
      translationLanguageCode: "en"
    }),
    /^transcript:dQw4w9WgXcQ:es:\.es:translated:translated:en:[a-f0-9]+$/
  );

  assert.notEqual(
    transcript.createTranscriptCacheKey("dQw4w9WgXcQ", chineseTrack),
    transcript.createTranscriptCacheKey("dQw4w9WgXcQ", englishTrack)
  );
});

