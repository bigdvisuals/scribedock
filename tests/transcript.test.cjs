test("saves, loads, and clears the preferred transcript language", async () => {
  const values = {};
  const storageArea = {
    get(key, callback) {
      callback({ [key]: values[key] });
    },
    set(nextValues, callback) {
      Object.assign(values, nextValues);
      callback();
    },
    remove(key, callback) {
      delete values[key];
      callback();
    }
  };

  await transcript.savePreferredTranscriptLanguage("ES_419", storageArea);
  assert.equal(await transcript.getPreferredTranscriptLanguage(storageArea), "es-419");

  await transcript.clearPreferredTranscriptLanguage(storageArea);
  assert.equal(await transcript.getPreferredTranscriptLanguage(storageArea), "");
});

