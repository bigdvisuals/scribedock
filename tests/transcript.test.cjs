function createPlayerResponse(videoId, baseUrl, label) {
  return {
    captions: {
      playerCaptionsTracklistRenderer: {
        captionTracks: [
          {
            baseUrl,
            languageCode: "en",
            kind: "asr",
            name: {
              simpleText: label || "English (auto-generated)"
            }
          }
        ]
      }
    },
    videoDetails: {
      videoId
    }
  };
}

