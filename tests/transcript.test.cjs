test("caption discovery uses the live player response before stale script tags", async () => {
  const originalPlayerResponse = global.ytInitialPlayerResponse;
  const liveResponse = createPlayerResponse("dQw4w9WgXcQ", "https://www.youtube.com/api/timedtext?v=live&lang=en", "Live English");
  const scriptResponse = createPlayerResponse("dQw4w9WgXcQ", "https://www.youtube.com/api/timedtext?v=script&lang=en", "Script English");

  global.ytInitialPlayerResponse = liveResponse;

  try {
    const result = await transcript.detectTranscriptAvailability({
      videoId: "dQw4w9WgXcQ",
      document: {
        scripts: [
          {
            textContent: `var ytInitialPlayerResponse = ${JSON.stringify(scriptResponse)};`
          }
        ]
      },
      fetchFn: async () => {
        throw new Error("watch page fetch should not run");
      }
    });

    assert.equal(result.status, "available");
    assert.equal(result.source, "live-player-response");
    assert.equal(result.tracks[0].baseUrl, "https://www.youtube.com/api/timedtext?v=live&lang=en");
  } finally {
    global.ytInitialPlayerResponse = originalPlayerResponse;
  }
});

