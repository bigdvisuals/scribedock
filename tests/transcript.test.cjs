test("rejects expired transcript cache entries", () => {
  const now = 48 * 60 * 60 * 1000;
  const freshEntry = {
    rows: [{ text: "Cached row" }],
    timestamp: now - (23 * 60 * 60 * 1000)
  };
  const expiredEntry = {
    rows: [{ text: "Old row" }],
    timestamp: now - (25 * 60 * 60 * 1000)
  };

  assert.equal(transcript.isTranscriptCacheEntryFresh(freshEntry, now), true);
  assert.equal(transcript.isTranscriptCacheEntryFresh(expiredEntry, now), false);
  assert.equal(transcript.isTranscriptCacheEntryFresh({ timestamp: now }, now), false);
});

