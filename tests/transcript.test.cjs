test("falls back when browser Trusted Types blocks innerHTML entity decoding", () => {
  const originalDocument = global.document;

  global.document = {
    createElement: () => {
      const element = {};

      Object.defineProperty(element, "innerHTML", {
        set: () => {
          throw new TypeError("This document requires TrustedHTML assignment.");
        }
      });

      return element;
    }
  };

  try {
    const rows = transcript.parseTranscriptResponse('<transcript><text start="1" dur="2">Safe &amp; readable</text></transcript>');

    assert.equal(rows[0].text, "Safe & readable");
  } finally {
    global.document = originalDocument;
  }
});

test("parses JSON3 transcript events into timestamped rows", () => {
  const json = JSON.stringify({
    events: [
      {
        tStartMs: 17000,
        dDurationMs: 2400,
        segs: [
          { utf8: "Hello " },
          { utf8: "again" }
        ]
      },
      {
        tStartMs: 724000,
        segs: [
          { utf8: "long video" }
        ]
      }
    ]
  });
  const rows = transcript.parseTranscriptResponse(json);

  assert.equal(rows[0].timestamp, "0:17");
  assert.equal(rows[0].text, "Hello again");
  assert.equal(rows[1].timestamp, "12:04");
  assert.equal(rows[1].text, "long video");
});

test("parses SRV3 timedtext XML into timestamped rows", () => {
  const xml = [
    "<timedtext>",
    "<body>",
    '<p t="17000" d="2400"><s>Hello </s><s>again</s></p>',
    '<p t="724000"><s>long video</s></p>',
    "</body>",
    "</timedtext>"
  ].join("");
  const rows = transcript.parseTranscriptResponse(xml);

  assert.equal(rows[0].timestamp, "0:17");
  assert.equal(rows[0].durationSeconds, 2.4);
  assert.equal(rows[0].text, "Hello again");
  assert.equal(rows[1].timestamp, "12:04");
  assert.equal(rows[1].text, "long video");
});

