test("tries fallback caption formats before failing", async () => {
  const requestedUrls = [];
  const rows = await transcript.fetchTranscriptTrack(
    {
      baseUrl: "https://www.youtube.com/api/timedtext?v=abc&lang=en&fmt=vtt"
    },
    async (url) => {
      requestedUrls.push(url);

      if (url.includes("fmt=json3")) {
        return {
          ok: true,
          text: async () => JSON.stringify({
            events: [
              {
                tStartMs: 17000,
                dDurationMs: 2000,
                segs: [
                  { utf8: "Fallback worked" }
                ]
              }
            ]
          })
        };
      }

      return {
        ok: true,
        text: async () => "<transcript></transcript>"
      };
    }
  );

  assert.deepEqual(requestedUrls, [
    "https://www.youtube.com/api/timedtext?v=abc&lang=en&fmt=vtt",
    "https://www.youtube.com/api/timedtext?v=abc&lang=en&fmt=json3"
  ]);
  assert.equal(rows[0].text, "Fallback worked");
});

test("logs sanitized caption fetch attempts when a debug logger is provided", async () => {
  const messages = [];

  await transcript.fetchTranscriptTrack(
    {
      baseUrl: "https://www.youtube.com/api/timedtext?v=abc&lang=en&fmt=json3&signature=secret&expire=123"
    },
    async () => ({
      ok: true,
      text: async () => JSON.stringify({
        events: [
          {
            tStartMs: 1000,
            segs: [
              { utf8: "Logged safely" }
            ]
          }
        ]
      })
    }),
    {
      debugLogger: (message) => messages.push(message)
    }
  );

  const combinedMessages = messages.join("\n");

  assert.ok(messages.length > 1);
  assert.match(combinedMessages, /signature=REDACTED/);
  assert.match(combinedMessages, /expire=REDACTED/);
  assert.doesNotMatch(combinedMessages, /secret/);
});

